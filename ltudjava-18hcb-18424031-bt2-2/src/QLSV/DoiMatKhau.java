    private boolean kiemtra(String passnow, String passnew, String passnewagain) {
        boolean kiemtra = false;
        StringBuilder msgErr = new StringBuilder();

        if (passnow.equals("")) {
            kiemtra = true;
            msgErr.append("Vui lòng nhập mật khẩu hiện tại.\n");
        }

        if (passnew.equals("")) {
            kiemtra = true;
            msgErr.append("Vui lòng nhập mật khẩu mới.\n");
        }
        if (passnewagain.equals("")) {
            kiemtra = true;
            msgErr.append("Vui lòng nhập mật khẩu mới lần nửa.");
        }
        if(kiemtra==true)
        {
            JOptionPane.showMessageDialog(this, msgErr);
        }
        
        return kiemtra;
    }
    
