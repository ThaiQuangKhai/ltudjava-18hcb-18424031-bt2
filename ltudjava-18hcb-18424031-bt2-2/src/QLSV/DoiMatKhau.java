    private void bt_luuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_luuActionPerformed
        // TODO add your handling code here:
        String passnow=new String(txt_passwordnow.getPassword());
        String passnew=new String(txt_passwordnew.getPassword());
        String passnewagain=new String(txt_passwordnewagain.getPassword());

        if (account!=null && account.getPassword().equals(passnow)) 
        {
            if(passnew.equals(passnewagain))
            {
                Account accountUpdate = account;
                accountUpdate.setPassword(passnew);
                boolean updateAccount = AccountDAO.updateAccount(accountUpdate);
                if(AccountDAO.updateAccount(account)==true)
                {
                    JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công.");
                    GiaoVu gv = new GiaoVu();
                    gv.setVisible(true);
                    this.setVisible(false);
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Mật khẩu mới và nhập lại mật khẩu mới không trùng khớp.");
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Mật khẩu hiện tại chưa đúng.");
        }

    }//GEN-LAST:event_bt_luuActionPerformed

